def generate_all_reports(results, calibration, verification, config, synterra_query=None):
    """Generate all output files."""
    output_dir = config.output.dir
    Path(output_dir).mkdir(parents=True, exist_ok=True)

    generate_results_csv(results, f"{output_dir}/{config.output.results_csv}", synterra_query)
    generate_ranked_md(results, f"{output_dir}/{config.output.ranked_md}", synterra_query)
    generate_pareto_plot(results, f"{output_dir}/{config.output.pareto_plot}")

    if calibration:
        generate_calibration_report(calibration, f"{output_dir}/{config.output.calibration_report}")

    if verification:
        generate_verification_report(verification, f"{output_dir}/{config.output.verification_report}")


def _params_short(params: dict) -> str:
    """Short config string."""
    parts = []
    dopant = params.get("dopant_type", "none")
    if dopant != "none":
        parts.append(f"{dopant}{int(params.get('dopant_percent', 0))}")
    else:
        parts.append("Ni")
    parts.append(f"{int(params.get('n_layers', 2))}L")
    parts.append(f"{int(params.get('vacancy_percent', 0))}%v")
    if params.get("n_doping_percent", 0) > 0:
        parts.append(f"{int(params['n_doping_percent'])}%N")
    return " ".join(parts)


def _params_to_elements(params: dict) -> list[str]:
    """Extract element list from candidate params."""
    elements = ["Ni"]
    dopant = params.get("dopant_type", "none")
    if dopant and dopant != "none":
        elements.append(str(dopant))
    return elements


def _generate_insights(successful: list) -> str:
    """Auto-generate insights from results."""
    insights = []

    # Best dopant
    by_dopant = {}
    for c in successful:
        d = c.params.get("dopant_type", "none")
        score = sum(c.scores.values())
        by_dopant.setdefault(d, []).append(score)

    if by_dopant:
        best_dopant = max(by_dopant, key=lambda k: np.mean(by_dopant[k]))
        insights.append(f"- **Best dopant:** {best_dopant} (avg score {np.mean(by_dopant[best_dopant]):.3f})")

    # Best layers
    by_layers = {}
    for c in successful:
        l = int(c.params.get("n_layers", 2))
        score = sum(c.scores.values())
        by_layers.setdefault(l, []).append(score)

    if by_layers:
        best_layers = max(by_layers, key=lambda k: np.mean(by_layers[k]))
        insights.append(f"- **Best shell layers:** {best_layers} (avg score {np.mean(by_layers[best_layers]):.3f})")

    return "\n".join(insights) if insights else "- Insufficient data for insights"
